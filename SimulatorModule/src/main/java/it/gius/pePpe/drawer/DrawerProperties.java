package it.gius.pePpe.drawer;

public class DrawerProperties {
	
	public int colorBackground = 0;
	
	public int colorFillShapes = 0;

	public int colorStrokeShapes = 0;
	
	public int colorSprings = 0;
	
	public int colorAABB  = 0;
	
	public int colorBoundCircles = 0;
	
	public int colorDistances = 0;

	public int getColorBackground() {
		return colorBackground;
	}

	public void setColorBackground(int colorBackground) {
		this.colorBackground = colorBackground;
	}

	public int getColorFillShapes() {
		return colorFillShapes;
	}

	public void setColorFillShapes(int colorFillShapes) {
		this.colorFillShapes = colorFillShapes;
	}

	public int getColorStrokeShapes() {
		return colorStrokeShapes;
	}

	public void setColorStrokeShapes(int colorStrokeShapes) {
		this.colorStrokeShapes = colorStrokeShapes;
	}

	public int getColorSprings() {
		return colorSprings;
	}

	public void setColorSprings(int colorSprings) {
		this.colorSprings = colorSprings;
	}

	public int getColorAABB() {
		return colorAABB;
	}

	public void setColorAABB(int colorAABB) {
		this.colorAABB = colorAABB;
	}

	public int getColorBoundCircles() {
		return colorBoundCircles;
	}

	public void setColorBoundCircles(int colorBoundCircles) {
		this.colorBoundCircles = colorBoundCircles;
	}

	public int getColorDistances() {
		return colorDistances;
	}

	public void setColorDistances(int colorDistances) {
		this.colorDistances = colorDistances;
	}

	
}
